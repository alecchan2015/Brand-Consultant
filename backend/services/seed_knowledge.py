def import_seed_knowledge(db) -> int:
    """
    Import seed knowledge entries into the database.

    - Checks for existing entries by title to avoid duplicates.
    - Only imports new entries.
    - Returns the count of newly imported items.

    Args:
        db: SQLAlchemy database session.

    Returns:
        int: Number of newly imported knowledge entries.
    """
    # Import here to avoid circular imports at module level
    from models import AgentKnowledge  # noqa: E402

    # Fetch all existing titles in one query
    existing_titles = {
        row[0]
        for row in db.query(AgentKnowledge.title).filter(
            AgentKnowledge.is_active == True  # noqa: E712
        ).all()
    }

    imported_count = 0

    for entry in SEED_KNOWLEDGE:
        if entry["title"] in existing_titles:
            continue

        knowledge = AgentKnowledge(
            agent_type=entry["agent_type"],
            title=entry["title"],
            content=entry["content"],
            created_by=None,  # System seed data — no specific user
            is_active=True,
        )
        db.add(knowledge)
        imported_count += 1

    if imported_count > 0:
        db.commit()

    return imported_count
